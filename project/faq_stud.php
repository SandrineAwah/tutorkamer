  <p style="margin-left:30px;">Welcome to Tutor Finder!. Below are answers to some common questions that you may have about Tutor Finder.</p>

<p> <b>1. How do I find a tutor?</b> <br>
We have many registered tutors and cover most regions, subjects, levels and abilities.

Select a region and subject (or advanced search).
Click on Search Now.
Browse through the list of tutors who match your selection criteria.</p>

<p> <b>2. How do I contact a tutor?</b><br>
Contacting a tutor with tutor finder is easy as it provides both mail id and phone number of the tutor which can be used for future
communications
</p>

<p> <b>3. What should I discuss in the initial email message I send to the tutor?</b><br>
Specific subject(s) for which you need a tutor.
Student's grade level.
Describe special circumstances (e.g. student with special needs).
Whether you like the tutor to travel to your home.
How soon you need a tutor.
Include your telephone number if you like. This is totally up to you.</p>

<p> <b>4. What if there are no tutors listed for my search criteria?</b><br>
If there are no tutors listed for your search criteria, you are welcome to place request in the request tutor section by filling out  form Request a Tutor. We will list your request on our website and tutors in your area will be able to contact you directly.</p>

<p> <b>5. How many tutors should I contact?</b><br>
There may be a number of tutors listed for your search criteria. Please browse through each tutor's profile and contact those tutors who have more direct experience and expertise in the subject(s) you need help. It is a good idea to contact several tutors. This will greatly increase your chance of finding the right tutor.</p>

<p> <b>6. Tutor has not responded to my request. What should I do?</b><br>
Most tutors respond to your request very shortly. There might be a delay in a response to your request from a tutor. This could be due to a variety of reasons such as the tutor being on vacation or not having access to email. Some tutors are totally booked and sometimes don't bother to respond to a request.

If you have not heard from the tutor within a few days, please try another tutor.</p>

<p> <b>7. I need help in a subject that is not listed. What should I do?</b><br>
We have a comprehensive list of academic and non-academic subjects, and new subjects are added on a regular basis.</p>
